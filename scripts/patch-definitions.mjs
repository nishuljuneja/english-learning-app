for (const [word, example] of Object.entries(examples)) {
  // Clean the word key the same way vocabulary.ts does
  const key = word.split(/[,/]/)[0].trim().toLowerCase();
  if (defs[key] && !defs[key].e) {
    defs[key].e = example;
    patchedExamples++;
  }
}
console.log(`Patched ${patchedExamples} example sentences`);

